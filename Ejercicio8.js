/*Declara una clase Tienda que permita registrar:
Nombre de la tienda,
Direccion de la tienda,
Propietario de la tienda,
Rubro de la tienda*/
class Tienda {
    constructor(Nombre, Direccion, Propietario, Rubro) {
        this.Nombre = Nombre;
        this.Direccion = Direccion;
        this.Propietario = Propietario;
        this.Rubro = Rubro;
    }
}

